import Foundation
import UIKit

extension UIColor {
  
  class func red() -> UIColor {
    return UIColor(red: 1, green: 0.03, blue: 0.1, alpha: 1)
  }
  
  class func green() -> UIColor {
    return UIColor(red: 0.28, green: 0.67, blue: 0.51, alpha: 1)
  }
  
  class func blue() -> UIColor {
    return UIColor(red: 0, green: 0.55, blue: 0.85, alpha: 1)
  }
  
  class func grayD() -> UIColor {
    return UIColor(red: 0.25, green: 0.19, blue: 0.18, alpha: 1)
  }
  
  class func gray() -> UIColor {
    return UIColor(red: 0.6, green: 0.6, blue: 0.6, alpha: 1)
  }
  
  class func grayL() -> UIColor {
    return UIColor(red: 0.93, green: 0.93, blue: 0.92, alpha: 1)
  }
  
  class func grayLL() -> UIColor {
    return UIColor(red: 0.98, green: 0.98, blue: 0.98, alpha: 1)
  }
  
}